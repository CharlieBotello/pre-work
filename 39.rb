# Create a function that takes in a number as its input and returns the square of that number. For example, if the input is 4, the output should be 16, which is 4 * 4. If the input is 5, the output should be 25, which is 5 * 5.

def squared(input_number)
  input_number = input_number ** 2
  return input_number
end 


p squared(8)


# Create a function that takes in a string as its input and returns a tripled version of the string. So if the input is “cat”, the output should be “catcatcat”.


def triple_string(input_string)
  input_string = input_string * 3
end 

p triple_string("cat")








